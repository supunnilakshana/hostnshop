// scripts/configure-branding.js
const fs = require("fs");
const path = require("path");

function configureBranding() {
  const appName = process.env.NEXT_PUBLIC_APP_NAME || "HostNShop";
  const appDescription =
    process.env.NEXT_PUBLIC_APP_DESCRIPTION || "Generated by create next app";

  // Create a branding.json file that can be loaded by the app components
  try {
    const branding = {
      appName: process.env.NEXT_PUBLIC_APP_NAME || "HostNShop",
      appDescription:
        process.env.NEXT_PUBLIC_APP_DESCRIPTION ||
        "Generated by create next app",
      contactEmail:
        process.env.NEXT_PUBLIC_CONTACT_EMAIL || "contact@hostnshop.com",
      contactPhone:
        process.env.NEXT_PUBLIC_CONTACT_PHONE || "+1 (555) 123-4567",
      logoPath:
        process.env.NEXT_PUBLIC_LOGO_PATH || "/assets/images/HostNShop.png",
      faviconPath: process.env.NEXT_PUBLIC_FAVICON_PATH || "/favicon.ico",
    };

    const publicDir = path.join(process.cwd(), "public");
    if (!fs.existsSync(publicDir)) {
      fs.mkdirSync(publicDir, {recursive: true});
    }

    fs.writeFileSync(
      path.join(publicDir, "branding.json"),
      JSON.stringify(branding, null, 2)
    );
    console.log("Created branding.json with configuration");
  } catch (error) {
    console.error("Error creating branding.json:", error);
  }
}

configureBranding();
